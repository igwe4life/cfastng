class Product {
  final String title;
  final String description;
  final String image;
  final String price;
  final String date;
  final String time;
  final String itemUrl;
  final String classID;
  final String location;

  Product({
    required this.title,
    required this.description,
    required this.image,
    required this.price,
    required this.date,
    required this.time,
    required this.itemUrl,
    required this.classID,
    required this.location,
  });
}
